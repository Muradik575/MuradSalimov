package day4_printmitives_operators;

public class StringsConcat {
	public static void main(String[] args) {
		String firstName = "Murad";
		System.out.println("My name is Murad" + firstName);
		
		String city = "BAKU";
		System.out.println("I was born in " + city);
		
		String job = "SDET";
		String Company = "Apple";
		System.out.println("I work as  " + job+ " in "+ Company );
		
		int zipcode = 20850;
		System.out.println("I live in " + zipcode + " zipcode");
		
		int num1 = 10;
		int num2 = 20;
		System.out.println(num1+""+num2);
		
	}

}
