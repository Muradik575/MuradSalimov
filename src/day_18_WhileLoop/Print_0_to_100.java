package day_18_WhileLoop;

public class Print_0_to_100 {

public static void main(String[] args) {
	
	int counter = 0;
	
	while(counter <= 100) {  // as long as the condition if true will be looping
		System.out.println(counter); 
		counter++; // increasing value by 1 every time when its looping and assigning the value to a variable
	}
	
	//System.out.println(counter);
	
	
	
	
	
	
	
}	
	
	
}
